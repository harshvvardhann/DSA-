package TwopointeSlidingWindow;

class SubstringContainingAll3Characters {
    public int numberOfSubstrings(String s) {
        int arr[] = new int[3];
        int left = 0;
        int sum = 0;    
        int n = s.length();

        for(int right=0;right<s.length();right++){
            arr[s.charAt(right)-'a']++;
            while(arr[0]>0 && arr[1]>0 && arr[2]>0){
                sum += (n-right);
                arr[s.charAt(left)-'a']--;
                left++;
            }
        }
        return sum;
    }
}